#include <unistd.h>

void ft_print_comb(void)
{
  int i = 0;

  while (i <= 7)
  {
    int j = i + 1;
    while (j <= 8)
    {
      int k = j + 1;
      while (k <= 9)
      {
        if (k != 2)
        {
          write(1, ", ", 2);
        }
        write(1, (char[]){i + 48, j + 48, k + 48}, 3);
        k++;
      }
      j++;
    }
    i++;
  }
}